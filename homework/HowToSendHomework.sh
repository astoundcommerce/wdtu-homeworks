## ЯК ЗДАВАТИ ДОМАШНІ ЗАВДАННЯ
##
## Після закінчення виконання завдання необхідно виконати 3 команди в консолі:
## 1. Змінені або додані файли додати до системи контролю версій GIT

git add --all

## 2. Зафіксувати зміни (закомітити) і вказати коментар до виконаного завдання

git commit -m "Ваш коментар"

## 3. Відправити на сервер для перевірки викладачами:

git push